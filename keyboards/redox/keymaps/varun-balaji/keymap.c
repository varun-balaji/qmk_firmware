// Tap repeat hold for homerow keys
bool get_tapping_force_hold(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case GUI_A:
            return true;
        case ALT_S:
            return true;
        case CTL_D:
            return true;
        case SFT_F:
            return true;
        case GUI_SCL:
            return true;
        default:
            return false;
    }
}
